public class Average {
    public static void main(String[] args) {
        int num1 = 15;
        int num2 = 25;
        int num3 = 35;
        double average = (num1 + num2 + num3) / 3.0;
        System.out.println("The average of " + num1 + ", " + num2 + ", and " + num3 + " is: " + average);
    }
}
